    def prepare_tsv_data(self, tenders_data: List[Dict[str, Any]]) -> str:
        """
        Prepare TSV data for the language model.
        
        Args:
            tenders_data: List of dictionaries containing tender information
            
        Returns:
            TSV string
        """
        tsv_lines = []
        
        for tender in tenders_data:
            tender_id = tender.get('شماره مناقصه در هزاره', '')
            title = tender.get('عنوان', '')
            description = tender.get('شرح آگهی', '')
            
            # Clean the data
            title = str(title).replace('\t', ' ').replace('\n', ' ')
            description = str(description).replace('\t', ' ').replace('\n', ' ')
            
            # Create TSV line
            tsv_line = f"{tender_id}\t{title}\t{description}"
            tsv_lines.append(tsv_line)
        
        return '\n'.join(tsv_lines)
    
    def classify_departments(self, tenders_data: List[Dict[str, Any]]) -> Dict[str, str]:
        """
        Classify tenders by department using the language model.
        
        Args:
            tenders_data: List of dictionaries containing tender information
            
        Returns:
            Dictionary mapping tender IDs to department labels
        """
        try:
            logger.info(f"Classifying {len(tenders_data)} tenders by department")
            
            # Prepare TSV data
            tsv_data = self.prepare_tsv_data(tenders_data)
            
            # Get system prompt
            system_prompt = self.get_system_prompt()
            
            # Send the request to the language model with timeout and retry logic
            logger.info("Sending request to language model")
            
            max_retries = 3
            timeout_seconds = 120  # 2 minutes timeout
            
            for attempt in range(max_retries):
                try:
                    logger.info(f"Attempt {attempt + 1}/{max_retries}")
                    
                    response = self.client.chat.completions.create(
                        model=self.model,
                        messages=[
                            {"role": "system", "content": system_prompt},
                            {"role": "user", "content": tsv_data}
                        ],
                        temperature=0.1,  # Changed from 1 to 0.1 for more consistent JSON output
                        max_tokens=2000,
                        timeout=timeout_seconds  # Add timeout
                    )
                    
                    logger.info("✅ Received response from language model")
                    break
                    
                except Exception as e:
                    logger.error(f"❌ Attempt {attempt + 1} failed: {str(e)}")
                    if attempt < max_retries - 1:
                        import time
                        wait_time = (attempt + 1) * 10  # Wait 10, 20, 30 seconds
                        logger.info(f"⏳ Waiting {wait_time} seconds before retry...")
                        time.sleep(wait_time)
                    else:
                        logger.error("All retry attempts failed")
                        raise
            
            # Parse the response
            logger.info("Parsing language model response")
            response_text = response.choices[0].message.content
            
            # Check if response is None or empty
            if not response_text:
                logger.error("AI returned empty/null response")
                logger.warning("Returning empty result due to empty AI response")
                return {}
            
            response_text = response_text.strip()
            
            # ADD DEBUG LOGGING
            logger.info(f"AI Response length: {len(response_text)}")
            logger.info(f"AI Response (first 500 chars): {response_text[:500]}")
            
            # Check if response is still empty after stripping
            if not response_text:
                logger.error("AI response is empty after stripping whitespace")
                logger.warning("Returning empty result due to empty response")
                return {}
            
            # Extract JSON from the response
            try:
                # Try to find JSON content in the response
                json_start = response_text.find('{')
                json_end = response_text.rfind('}') + 1
                
                if json_start >= 0 and json_end > json_start:
                    json_content = response_text[json_start:json_end]
                    logger.info(f"Extracted JSON content: {json_content}")
                    department_map = json.loads(json_content)
                else:
                    # If no JSON found, try parsing the entire response
                    logger.info("No JSON brackets found, trying to parse entire response")
                    department_map = json.loads(response_text)
                
                logger.info(f"Successfully parsed department classifications for {len(department_map)} tenders")
                return department_map
                
            except json.JSONDecodeError as e:
                logger.error(f"Failed to parse JSON from response:")
                logger.error(f"JSON Error: {e}")
                logger.error(f"Response content: {repr(response_text)}")
                logger.error(f"Response length: {len(response_text)}")
                
                # Try additional JSON cleaning
                cleaned_response = response_text.strip()
                if cleaned_response.startswith('```json'):
                    cleaned_response = cleaned_response[7:]
                if cleaned_response.endswith('```'):
                    cleaned_response = cleaned_response[:-3]
                cleaned_response = cleaned_response.strip()
                
                if cleaned_response:
                    try:
                        logger.info(f"Trying cleaned response: {cleaned_response}")
                        department_map = json.loads(cleaned_response)
                        logger.info("Successfully parsed cleaned JSON!")
                        return department_map
                    except json.JSONDecodeError:
                        logger.error("Cleaned JSON parsing also failed")
                
                # Final fallback: create a basic classification
                logger.warning("Creating fallback classifications")
                fallback_map = {}
                for tender in tenders_data:
                    tender_id = str(tender.get('شماره مناقصه در هزاره', ''))
                    if tender_id:
                        fallback_map[tender_id] = "سایر"
                return fallback_map
            
        except Exception as e:
            logger.error(f"Error classifying tenders by department: {e}")
            # Return empty dict instead of raising to prevent pipeline failure
            logger.warning("Returning empty result due to error")
            return {}
    
