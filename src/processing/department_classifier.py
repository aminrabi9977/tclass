"""
Department classifier module for classifying tender data by department.
"""
import os
import json
import pandas as pd
from typing import List, Dict, Any, Optional
import logging
from openai import OpenAI

logger = logging.getLogger(__name__)

class DepartmentClassifier:
    """
    Classifies tender data by department using a language model.
    """
    
    def __init__(self, api_key: str, base_url: str = None):
        """
        Initialize the department classifier.
        
        Args:
            api_key: API key for the OpenAI API
            base_url: Base URL for the OpenAI API (optional)
        """
        try:
            # Initialize the OpenAI client
            if base_url:
                self.client = OpenAI(api_key=api_key, base_url=base_url)
            else:
                self.client = OpenAI(api_key=api_key)
            
            # Fixed model name - should be "gpt-4o" not "gpt-5-mini"
            self.model = "gpt-4o"
            logger.info("Department classifier initialized successfully")
        except Exception as e:
            logger.error(f"Error initializing department classifier: {e}")
            raise
    
