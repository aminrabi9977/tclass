#         ##قابلیت های معاونت زیرساخت:
#         خطوط انتقال نیرو:
#         در گروه خطوط انتقال نیرو، کلیه امور مربوط به طراحی، مدلسازی، ارائه خدمات مهندسی و مدیریت فنی و مالی در کلیه مراحل پروژه‌های مرتبط با شبكه‌های انتقال و فوق‌توزیع نیرو و با محوریت خطوط انجام می‌گردد. این فعالیت‌ها شامل مواردی همچون طراحی پایه و تفصیلی خطوط انتقال نیرو در كلیه سطوح ولتاژی انتقال، فوق‌توزیع و توزیع از 20 تا 765 کیلوولت (و از جمله خطوط 765 کیلوولت و 500 کیلوولت HVAC وHVDC ) در داخل و خارج از كشور، طراحی و انجام مطالعات کابلهای فشار قوی در مسیرهای خشكی (دفن زیرزمینی) و آبی (زیردریایی)، تونلهای كابلی آدم‌رو و تونل های حفاری جهت‌دار افقی (HDD)، تهیه نقشه‌های عین ساخت (As-Built) خطوط انتقال برق موجود، جایگزینی و نصب سیم محافظ حاوی فیبر نوری (OPGW) بر روی خطوط انتقال و همچنین کابل فیبر نوری (ADSS) بر روی زیرساخت‌های شبکه‌های توزیع و روشنایی، مسیریابی هوشمند خطوط مبتنی بر عكس‌های دقیق ماهواره‌ای، نقشه‌برداری مسیر خطوط به صورت زمینی و هوایی (با كمك تكنولوژی LiDAR و پهپاد)، شبیه‌سازی و طراحی برج‌های مشبك و تلسكوپی بهینه جهت خطوط انتقال و توزیع نیرو (با كمك نرم افزارهای PLS-Pole، Tower و MS-Tower)، مطالعات طرح‌های جامع برقرسانی، مطالعات فنی و مالی طرح های اتصال خطوط و پست‌ها به شبكه برق، مطالعات فنی خطوط موجود و ارائه راهکارهای مناسب جهت رفع مسائل و چالش‌های بهره‌برداری، مطالعات افزایش ظرفیت و ارتقاء شبکه، مطالعات ارتقاء ولتاژ و ظرفیت شبكه با كمك هادی‌های پرظرفیت، برگزاری دوره‌های آموزشی برای نرم‌افزارهای تخصصی و تدوین و تشریح استانداردهای بین‌المللی در داخل و خارج از کشور می‌گردد.
#         از مهمترین نرم‌افزارهای مورد استفاده در این گروه، می‌توان به نرم‌افزارهای PLS-CADD جهت طراحی و برج گذاری خطوط، Tower و MS-Tower جهت طراحی برج‌های مشبك، PLS-Pole جهت طراحی برج‌های تلسكوپی، Caisson جهت طراحی فونداسیون‌ها، Digsilent و Cyme جهت مطالعات سیستم قدرت و برنامه‌ریزی توسعه شبكه، Arc-GIS جهت مجموعه فعالیت‌های مبتنی بر GIS و مسیریابی دقیق خطوط و چندین نرم‌افزار عمومی و تخصصی دیگر اشاره نمود.
        
#         پست‌های فشار قوی:
#         این گروه با استفاده از مطالعات پایه، مفهومی و طراحی تفصیلی در كلیه سطوح ولتاژی نسبت به انجام خدمات مهندسی، نظارت عالیه و نظارت كارگاهی پست‌های فشار قوی و فشار متوسط در برق‌های منطقه‌ای و صنایع مختلف در داخل و خارج از كشور اقدام نموده است. از جمله مهمترین فعالیت‌ها در بخش طراحی می‌توان به انجام مطالعات و طراحی سیستم برق‌رسانی برای طرح‌های جامع در صنایع مختلف، تنظیم رله مجتمع‌های صنعتی، طراحی و مهندسی سیستم HVDC در سطح ولتاژ 500 ± كیلوولت، طراحی پست‌های نوع GIS و AIS به صورت هایبرید، طراحی تفصیلی و مهندسی (بخش E) از قراردادهای EPC در پست‌های انتقال و نیروگاهی، طراحی و پیاده‌سازی نقشه‌های اجرایی جانمایی و مقاطع پست ها به صورت سه بعدی (3D)، پیاده‌سازی روش‌های مدرن مدیریت سرمایه (Asset Management)، انجام مطالعات جامع سیستم و حالت گذرا با نرم‌افزارهای معتبر بین‌المللی، مطالعات مربوط به اخذ وام های بین‌المللی در پروژه‌های ملی برق، برگزاری دوره‌های آموزشی در خصوص طراحی، مدیریت پروژه، بهره‌برداری و اپراتوری پست‌های فشار قوی، تهیه نقشه‌های برابر ساخت (As Built) سه بعدی با استفاده از تکنولوژی لیزراسکن زمینی در پست‌های فشار قوی، به كارگیری روش‌های نوین در طراحی و اجرای پروژه‌ها با استفاده از ساختارهای فنی و اقتصادی با در نظر گرفتن شرایط زیست محیطی و مطالعات میدانی، پست‌های دیجیتال و Unmanned نمودن پست‌ها، استفاده از ابزار واقعیات مجازی و واقعیت افزوده (Augmented Reality & Virtual Reality) در طراحی پست‌ها، تعمیر و نگهداری پست‌ها (PM) و مطالعه و برطرف نمودن مسائل و چالش‌های بهره‌برداری پست‌ها  اشاره نمود.
#         گروه عمران و شهرسازی
#         این گروه با مطالعات پایه، مفهومی و طراحی تفصیلی پروژه‌های امکان‌سنجی، طرح‌های جامع شهری و مناطق ویژه اقتصادی، شهرسازی، ساختمانی و صنعتی بر اساس آخرین آیین نامه‌های داخلی و خارجی نسبت به انجام خدمات مدیریت طرح، مهندسی، نظارت عالیه و نظارت کارگاهی اقدام نموده است و همچنین به عنوان بازوی تخصصی و مهندسی در حوزه فعالیت‌های ساختمانی و مورد نیاز سایر گروه‌های معاونت و عمدتا در زمینه ارائه خدمات مهندسی مرتبط با خطوط انتقال نیرو، پست‌های فشار قوی، شبکه‌های توزیع، ساختمان‌ها و مراکز دیسپاچینگ و دکل‌های مخابراتی فعالیت میکند.
#         از سویی دیگر با ارائه خدمات مشاوره و مهندسی، طراحی پایه و تفصیلی، ارائه خدمات نظارت کارگاهی و عالیه و مدیریت طرح در پروژه‌های شهری، صنعتی، اداری، تجاری، مسکونی، درمانی، بیمارستانی، مطالعات زیر ساخت‌های فرودگاه، مطالعات ترافیكی ساعت اوج، طراحی Landside شامل Accessibility و Terminal Passenger، طراحی Airside شامل Runways ، Apron و Taxiway، مطالعات پدافند غیرعامل و سایر سازه های متعارف و غیرمتعارف، طرح های توسعه و تغییر کاربری ساختمان‌ها با استفاده از تکنولوژی‌های نوین و همچنین ارائه خدمات مهندسی در زمینه‌های ژئورادار، ارگونومی ساختمان ها جهت اپراتوری سیستم‌ها، مدیریت انرژی ساختمان در فازهای طراحی، اجرا و بهره برداری، ارزیابی ریسک سیلاب در ساختگاه پروژه، ساختمان های سبز و ... نقش پررنگی در مجموعه معاونت زیر ساخت دارد.
#         شبکه های توزیع نیرو:
#         این گروه طراحی پایه و تفصیلی در شبکه‌های توزیع نیرو (سطوح ولتاژ فشار متوسط و ضعیف)، انجام مطالعات شبکه، تدوین طرح‌های جامع برق‌رسانی، بررسی قابلیت اطمینان و تاب‌آوری، هماهنگی حفاظی شبکه، بهبود کیفیت توان، کاهش تلفات، جایابی کلیدهای اتوماسیون، طراحی و اصلاح روشنایی معابر، اتصال به منابع تجدید پذیر و تولیدات پراکنده، بهینه سازی و مهندسی در پروژه‌های طراحی و نظارت شبکه‌های توزیع در داخل و خارج از کشور را برای صنایع مختلف برعهده‌دارد و همچنین با استفاده از آخرین نرم‌افزارهای مطالعاتی روز دنیا و مبتنی بر GIS، اقدام به انجام محاسبات پخش بار، اتصال کوتاه، برآورد بار، جایابی پست‌های توزیع، فوق توزیع و طراحی، مسیریابی و بازآرایی بهینه شبکه می‌نماید و عملا با طراحی بهینه خود و اعمال روش‌های نوین در پروژه‌ها، نقش قابل ملاحظه ای در کاهش زمان و قیمت تمام شده پروژه‌ها داشته است. همچنین مباحث مطرح در شبكه‌های پیشرفته توزیع از قبیل هوشمندسازی و ناوگان حمل و نقل برقی نیز در دستورکار این گروه قرار دارد.
#         فازهای اجرایی پروژه‌ها:
