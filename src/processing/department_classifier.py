#         ##حوزه کاری معاونت ارتباطات، فناوری اطلاعات و هوشمندسازی:
#         مهندسین مشاور موننکو ایران بر مبنای تجربیات خود در صنایع و سازمان های مختلف کشور، به ارائه خدمات مشاوره ای در حوزه فاوا (فناوری  اطلاعات و ارتباطات)، اسکادا و هوشمندسازی می پردازد. در همین راستا، در همين راستا، در زمینه‌های طراحی و مهندسی مراكز دیسپاچینگ و شبكه‌های مخابراتی در داخل و خارج از کشور شامل اسکادا، مانیتورینگ و دیسپاچینگ، شبکه‌ها و سیستم‌های مخابراتی، سیستم‌های اندازه‌گیری و شبكه هوشمند ارائه خدمات نموده و با در‌اختیار داشتن امكانات گسترده از جمله كادر فنی مجرب، توان نرم‌افزاری و سخت‌افزاری و استفاده از جدیدترین فناوری‌های روز دنیا و همچنین با تكیه بر تجارب ارزنده خود در پروژه‌های انجام شده درچند دهه اخیر، انجام كلیه مراحل خدمات مهندسی و مشاوره، طراحی‌های پایه، مفهومی و تفصیلی با جزئیات آن، آموزش‌های تخصصی، نظارت عالیه و كارگاهی و مدیریت بسیاری از پروژه‌های حساس و با اهمیت صنعت برق و سایر صنایع را بعهده دارد. داشتن تیم‌های تخصصی در زمینه‌های مختلف موبایل، فیبر نوری، شبکه‌های هوشمند و سیگنالینگ متمایز کننده این مهندسین در ارائه خدمات ویژه با کارفرمایان گردیده است. فعالیت‌های مربوط به این حوزه در 3 گروه مختلف صورت می پذیرد که عبارتند از:
#         ارتباطات و فناوری اطلاعات
#         مهندسين مشاور موننکو ایران از سال 1380 فعالیت خود را در حوزه مخابرات صنعت برق آغاز نمود و پروژه‌های بسیاری را در حوزه‌های امكان‌سنجی، طراحی و نظارت عالیه و كارگاهی راه‌اندازی شبكه مخابرات برق‌های منطقه‌ای و شبكه ملی برق كشور انجام داده و به تدریج موفق به گسترش فعالیت‌های خود در صنایع دیگر همانند آب و فاضلاب، نفت و گاز، پتروشیمی، حمل و نقل ریلی، هوایی و جاده‌ای و همچنین صنعت مخابرات گردید. بطوریکه پروژه‌های بسیاری را در زمینه‌های ارائه خدمات طراحی و مهندسی در فازهای امكان‌سنجی، طراحی مفهومی، پایه و تفصیلی، بازنگری طراحی،‌ تهیه مشخصات فنی، اسناد مناقصه و برگزاری آن، نظارت‌ عالیه و كارگاهی و تهیه طرح‌های تجاری، بر اساس استراتژی مشخص خود، به انجام رساند.
#         دیسپاچینگ و اتوماسیون
#         موننكو ایران در سال 1373 با هدف پاسخگویی به نیاز صنایع برق (تولید و انتقال و توزیع)، نفت و گاز، آب، حمل و نقل ریلی و جاده‌ای فعالیت های خود را در حوزه دیسپاچینگ و اتوماسیون گسترش داد که شامل طیف كاملی از انواع خدمات مهندسی، مشاوره و نظارت از جمله مطالعات امكان‌سنجی، طراحی مفهومی، طراحی پایه و تفصیلی، تهیه و ارائه مشخصات فنی و اسناد فنی و بازرگانی مناقصه، برگزاری مناقصات، انجام ارزیابی‌های فنی و مالی، بازبینی طراحی پیمانكاران و نظارت عالیه و كارگاهی بر اجرای طراحی می‌باشد. 
